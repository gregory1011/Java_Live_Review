package week02;

public class Operatros {
    public static void main(String[] args) {
        int age = 10;
        age++;
        System.out.println(age);
        System.out.println(age++);   // its print the value of the edge first, then it updates.
        System.out.println(age);

        int x = 15;
        int y = x++;
        System.out.println(x);   // x = 15
        System.out.println(y);   // y = 15



    }
}
